package com.chirag.weatherapp;

class Wind {
    private long deg;
    private double speed;

    public long getDeg() {
        return deg;
    }

    public void setDeg(long value) {
        this.deg = value;
    }

    public double getSpeed() {
        return speed;
    }

    public void setSpeed(double value) {
        this.speed = value;
    }
}
